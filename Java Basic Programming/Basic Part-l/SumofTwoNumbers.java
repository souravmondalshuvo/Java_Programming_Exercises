/*
Write a Java program to print the sum of two numbers.
Test Data:
74 + 36
Expected Output :
110
*/

public class SumofTwoNumbers {
    public static void main(String[] args) {
        int num1 = 74;
        int num2 = 36;
        int sum = num1 + num2;
        System.out.print("Sum of " + num1 + " and " + num2 + " is: ");
        System.out.println(sum);
    }
}